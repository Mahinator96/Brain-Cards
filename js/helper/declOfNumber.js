// Функция опрделения окончания слова пар(а,ы) в зависимости от числа
export const declOfNum = (n, titles) => {
  return `
    ${n} ${titles[
      // Условие на "пара"
      n % 10 === 1 && n % 100 !== 11 ? 0 :
      // Условие на пары
      (n % 10 >= 2 && n % 10 <= 4) && (n % 100 < 10 || n % 100 >= 20) ? 1 :
      // Всё остальное "пар"
      2 
    ]}`;
};